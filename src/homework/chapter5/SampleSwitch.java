package homework.chapter5;

public class SampleSwitch {
    public static void main(String[] args) {
        for (int i = 0; i < 6; i++) {
            switch (i){
                case 0:
                    System.out.println("ի հավասար է 0");
                    break;
                case 1:
                    System.out.println("ի հավասար է 1");
                    break;
                case 2:
                    System.out.println("ի հավասար է 4");
                    break;
                case 3:
                    System.out.println("ի հավասար է 3");
                    break;

                default:
                    System.out.println("ի մեծ է 3 ից");










            }

        }
    }
}
